                            return;
                          }

                          await UsersRecord.collection
                              .doc(user.uid)
                              .update(createUsersRecordData(
                                email: valueOrDefault<String>(
                                  _model.emailSignupFieldTextController.text,
                                  'example@gmail.com',
                                ),
                                createdTime: getCurrentTimestamp,
